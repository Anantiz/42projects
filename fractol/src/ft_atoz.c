#include "fractol.h"

int	ft_atoz(t_i *z, char *str)
{
	if (z == NULL)
		return (-1);
	z->r = ft_atodb(str);
	while (*str && (*str != 'i' || *str != 'I'))
		str++;
	str++;
	z->i = ft_atodb(str);
	return (0);
}
